!----------------------------------------------------------------------------------------
  subroutine guessmo(cmoa,cmob,overinv,h1mtrx,ortho,datajob,datamol,databasis,datacomp)
!----------------------------------------------------------------------------------------
!
! In   : h1mtrx  (one-electron Hamiltonian matrx)
!        ortho   (Orthogonalization matrix)
! Out  : cmoa    (Alpha initial guess orbitals)
!        cmob    (Beta initial guess orbitals)
! Inout: overinv (Overlap integral inverse matrix, and then work space)
!
      use modtype, only : typejob, typemol, typebasis, typecomp
      implicit none
      type(typejob),intent(in) :: datajob
      type(typemol),intent(in) :: datamol
      type(typebasis),intent(in) :: databasis
      type(typecomp),intent(inout) :: datacomp
      real(8),intent(in) :: h1mtrx(databasis%nao*(databasis%nao+1)/2), ortho(databasis%nao**2)
      real(8),intent(inout):: cmoa(databasis%nao**2), cmob(databasis%nao**2)
      real(8),intent(inout):: overinv(databasis%nao**2)
!
      if(datacomp%master) then
        write(datacomp%iout,'(" =====================")')
        write(datacomp%iout,'("   Guess calculation")')
        write(datacomp%iout,'(" =====================")')
      endif
!
      select case(datajob%guess)
        case('HUCKEL','HF')
          if(datacomp%master) &
&           write(datacomp%iout,'("   Guess MOs are generated using extended Huckel orbitals.")')
          call huckelguess(cmoa,overinv,datajob,datamol,databasis,datacomp)
          if(datacomp%master) write(datacomp%iout,*)
          if(datajob%scftype == 'UHF') call dcopy(databasis%nao**2,cmoa,1,cmob,1)
!
        case('DFTB')
          if(datacomp%master) write(datacomp%iout,'("   Guess MOs are generated using DFTB orbitals.")')
          call dftbguess(cmoa,overinv,datajob,datamol,databasis,datacomp)
          if(datacomp%master) write(datacomp%iout,*)
          if(datajob%scftype == 'UHF') call dcopy(databasis%nao**2,cmoa,1,cmob,1)
!
        case('UPDATE')
          call updatemo(cmoa,overinv,datajob,datamol,databasis,datacomp)
          if(datajob%scftype == 'UHF') call dcopy(databasis%nao**2,cmoa,1,cmob,1)
!
        case('CHECK')
          if(datacomp%master) &
&           write(datacomp%iout,'("   Guess MOs are read from checkpoint file and projected.",/)')
          call checkguess(cmoa,cmob,overinv,datajob,datamol,databasis,datacomp)
!
        case('HCORE')
          if(datacomp%master) &
&           write(datacomp%iout,'("   Guess MOs are generated by diagonalizing core Hamiltonian matrix.",/)')
          call hcoreguess(cmoa,h1mtrx,ortho,overinv,databasis%nao,datamol%nmo,datacomp)
          if(datajob%scftype == 'UHF') call dcopy(databasis%nao**2,cmoa,1,cmob,1)
        case default
          if(datacomp%master) &
&           write(*,'(" Error! This program does not support guess= ",a10,"!")') datajob%guess
          call iabort
      end select
!
      if(datacomp%master) then
        write(datacomp%iout,'(" If the calculation stops at the beginning of SCF, " &
&                 "increase the stack size.")')
        write(datacomp%iout,'(" bash: Write the following two lines in the ~/.bashrc file.")')
        write(datacomp%iout,'("       ulimit -s unlimited")')
        write(datacomp%iout,'("       export OMP_STACKSIZE=1G")')
        write(datacomp%iout,'(" tcsh: Write the following two lines in the ~/.tcshrc file.")')
        write(datacomp%iout,'("       unlimit")')
        write(datacomp%iout,'("       setenv OMP_STACKSIZE 1G",/)')
      endif
      return
end


!-------------------------------------------------------------------------
  subroutine huckelguess(cmo,overinv,datajob,datamol,databasis,datacomp)
!-------------------------------------------------------------------------
!
! Calculate initial guess MOs by extended Huckel
!
! In  : overinv (overlap integral inverse matrix)
! Out : cmo     (initial guess orbitals)
!
      use modparam, only : mxatom
      use modtype, only : typejob, typemol, typebasis, typecomp
      implicit none
      type(typejob),intent(in) :: datajob
      type(typemol),intent(in) :: datamol
      type(typebasis),intent(in) :: databasis
      type(typecomp),intent(inout) :: datacomp
      type(typebasis) :: dataguessbs
      integer :: nao_v, ntmp, i, j, nao_g, nao_g2
      real(8),intent(in):: overinv(databasis%nao**2)
      real(8),intent(out):: cmo(databasis%nao**2)
      real(8),allocatable :: hmo(:), overlap(:), work1(:), work2(:), eigen(:)
      real(8) :: coord_g(3,mxatom)
!
! Set basis functions
!
      dataguessbs%spher=.true.
      call setbasis_g(nao_v,ntmp,1,datajob,datamol,databasis,dataguessbs)
!
! Set coordinate
!
      do i= 1,datamol%natom
        do j= 1,3
          coord_g(j,i)= datamol%coord(j,i)
        enddo
      enddo
!
! Set required arrays
!
      nao_g = dataguessbs%nao
      nao_g2= nao_g*nao_g
      call memset(2*nao_g2+2*databasis%nao*nao_g+nao_g,datacomp)
      allocate(hmo(nao_g2),overlap(databasis%nao*nao_g),work1(databasis%nao*nao_g), &
&              work2(nao_g2),eigen(nao_g))
!
! Calculate Extended Huckel method
!
      call calchuckelg(hmo,eigen,coord_g,nao_v,datajob,datamol,databasis,dataguessbs,datacomp)
!
! Calculate overlap integrals between input basis and Huckel basis
!
      call calcover2(overlap,work1,coord_g,datajob%threshex, &
&                    datamol,databasis,dataguessbs,datacomp)
!
! Project orbitals from Huckel to SCF
!
      call projectmo(cmo,overinv,overlap,hmo,work1,work2,eigen,databasis%nao,nao_g,nao_g,datacomp)
      deallocate(hmo,overlap,work1, &
&                work2,eigen)
      call memunset(2*nao_g2+2*databasis%nao*nao_g+nao_g,datacomp)
      return
end


!-------------------------------------------------------------------------------------------------
  subroutine calchuckelg(hmo,eigen,coord_g,nao_v,datajob,datamol,databasis,dataguessbs,datacomp)
!-------------------------------------------------------------------------------------------------
!
! Driver of extended Huckel calculation for guess generation
!
! Out : hmo (extended Huckel orbitals)
!
      use modparam, only : mxatom
      use modtype, only : typejob, typemol, typebasis, typecomp
      implicit none
      type(typejob),intent(in) :: datajob
      type(typemol),intent(in) :: datamol
      type(typebasis),intent(in) :: databasis, dataguessbs
      type(typecomp),intent(inout) :: datacomp
      integer,intent(in) :: nao_v
      real(8),parameter :: zero=0.0D+00, one=1.0D+00
      real(8),intent(out) :: hmo(dataguessbs%nao*2), eigen(dataguessbs%nao)
      real(8),intent(in) :: coord_g(3,mxatom)
!
! Calculate overlap integrals
! (guess basis)x(guess basis)
!
      call calcover1(hmo,coord_g,datajob%threshex,dataguessbs)
!
! Set ionization potentials
!
      call huckelip(eigen,1,datajob%flagecp,datamol%natom,databasis%izcore,datamol%numatomic, &
&                   datacomp)
!
! Form extended Huckel matrix
!
      call formhuckel(hmo,eigen,dataguessbs%nao,nao_v)
!
! Diagonalize extended Huckel matrix
!
      call diag('V','U',dataguessbs%nao,hmo,dataguessbs%nao,eigen,datacomp)
!
      if(datacomp%master.and.(mod(datajob%iprint,10) >= 8)) then
        write(datacomp%iout,'("   Eigenvalues of Huckel guess")')
        write(datacomp%iout,'(4x,8f9.3)')eigen
      endif
!
      return
end


!----------------------------------------------------------------------------------------------
  subroutine calchuckelgcore(hmo,eigen,coord_g,datajob,datamol,databasis,datacorebs,datacomp)
!----------------------------------------------------------------------------------------------
!
! Driver of extended Huckel calculation for only core orbitals
!
! Out : hmo (extended Huckel orbitals)
!
      use modparam, only : mxatom
      use modtype, only : typejob, typemol, typebasis, typecomp
      implicit none
      type(typejob),intent(in) :: datajob
      type(typemol),intent(in) :: datamol
      type(typebasis),intent(in) :: databasis, datacorebs
      type(typecomp),intent(inout) :: datacomp
      real(8),parameter :: zero=0.0D+00, one=1.0D+00
      real(8),intent(in) :: coord_g(3,mxatom)
      real(8),intent(out) :: hmo(datacorebs%nao**2), eigen(datacorebs%nao)
!
! Calculate overlap integrals
! (guess basis)x(guess basis)
!
      call calcover1(hmo,coord_g,datajob%threshex,datacorebs)
!
! Set ionization potentials
!
      call huckelip(eigen,2,datajob%flagecp,datamol%natom,databasis%izcore,datamol%numatomic, &
&                   datacomp)
!
! Form extended Huckel matrix
!
      call formhuckel(hmo,eigen,datacorebs%nao,0)
!
! Diagonalize extended Huckel matrix
!
!     call diag('V','U',nao_gcore,hmo,nao_gcore,eigen,datacomp)
      call diag('V','U',datacorebs%nao,hmo,datacorebs%nao,eigen,datacomp)
!
      return
end


!-------------------------------------------------------------------------------------------
  subroutine projectmo(cmo,overinv,overlap,hmo,work1,work2,eigen,nao,nao_g,nmo_g,datacomp)
!-------------------------------------------------------------------------------------------
!
! Project orbitals from Huckel to SCF
!    C1= S11^-1 * S12 * C2 [C2t * S12t * S11^-1 * S12 * C2]^-1/2
!
! In  :  overinv (overlap integral inverse matrix of SCF basis set)
! Inout: overlap (overlap integral of guess and SCF basis sets)
!        hmo (extended Huckel orbitals)
! Out :  cmo (initial guess orbitals)
!
      use modtype, only : typecomp
      implicit none
      type(typecomp),intent(inout) :: datacomp
      integer,intent(in) :: nao, nao_g, nmo_g
      integer :: i, j, nskip
      real(8),parameter :: zero=0.0D+00, one=1.0D+00, small=1.0D-5
      real(8),intent(in) :: overinv(nao,nao)
      real(8),intent(inout) :: overlap(nao,nao_g), hmo(nao_g,nao_g)
      real(8),intent(out) :: cmo(nao,nao), work1(nao_g,nao_g), work2(nao_g,nao_g)
      real(8),intent(out) :: eigen(nao_g)
      real(8) :: eigeninv
!
! Calculate S12*C2
!
      call dgemm('N','N',nao,nmo_g,nao_g,one,overlap,nao,hmo,nao_g,zero,cmo,nao)
!
! Calculate S11^-1*S12*C2
!
      call dsymm('L','U',nao,nmo_g,one,overinv,nao,cmo,nao,zero,overlap,nao)
!
! Calculate C2t*S12t*S11^-1*S12*C2
!
      call dgemm('T','N',nmo_g,nmo_g,nao,one,cmo,nao,overlap,nao,zero,work1,nao_g)
!
! Calculate (C2t*S12t*S11^-1*S12*C2)^-1/2
!
      call diag('V','U',nmo_g,work1,nao_g,eigen,datacomp)
!
      nskip= 0
      do i= 1,nmo_g
        if(eigen(i) <= small) then
          nskip= nskip+1
        else
          cycle
        endif
      enddo
!
!$OMP parallel do private(eigeninv)
      do i= nskip+1,nmo_g
        eigeninv= one/sqrt(eigen(i))
        do j= 1,nmo_g
          work2(j,i)= work1(j,i)*eigeninv
        enddo
      enddo
!$OMP end parallel do
      call dgemm('N','T',nmo_g,nmo_g,nmo_g-nskip,one,work1(1,nskip+1),nao_g,work2(1,nskip+1), &
&                nao_g,zero,hmo,nao_g)
!
! Calculate C1
!
      call dgemm('N','N',nao,nmo_g,nmo_g,one,overlap,nao,hmo,nao_g,zero,cmo,nao)
!
      return
end


