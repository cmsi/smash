            case(-9:0)
            case default
              if(datacomp%master) write(*,'(" Error! This program supports up to Rn in huckelip.")')
              call iabort
          end select
        enddo
      endif
!
      return
end


!---------------------------------------------------
  subroutine formhuckel(huckel,energy,nao_g,nao_v)
!---------------------------------------------------
!
! Form extended Huckel matrix
!
! In   : energy (ionization potential)
! Inout: huckel[in] (overlap integral of guess basis set)
!              [out](extended Huckel Hamiltonian)
!
      implicit none
      integer,intent(in) :: nao_g, nao_v
      integer :: i, j
      real(8),parameter :: factor=0.875D+00  !(=1.75/2.0)
      real(8),parameter :: fdown=0.05D+00
      real(8),intent(in) :: energy(nao_g)
      real(8),intent(inout) :: huckel(nao_g,nao_g)
!
! Generate Huckel matrix from overlap integrals and ionization potentials
!
!$OMP parallel do schedule(static,1) private(j)
      do i= 1,nao_g
        do j= 1,i-1
          huckel(j,i)=factor*huckel(j,i)*(energy(i)+energy(j))
        enddo
        huckel(i,i)= energy(i)
      enddo
!$OMP end parallel do
!
! Scale down core-core and core-valence elements
!
!$OMP parallel do schedule(static,1) private(j)
      do i= nao_v+1,nao_g
        do j= 1,i-1
          huckel(j,i)= fdown*huckel(j,i)
        enddo
      enddo
!$OMP end parallel do
      return
end


!-------------------------------------------------------------
  subroutine calcover1(overlap,coord_g,threshex,dataguessbs)
!-------------------------------------------------------------
!
! Driver of overlap integral calculation
! (guess basis)x(guess basis)
!
! Out : overlap (overlap integral of guess basis set)
!
      use modparam, only : mxatom
      use modtype, only : typebasis
      implicit none
      type(typebasis),intent(in) :: dataguessbs
      integer :: ish, jsh
      real(8),intent(in) :: coord_g(3,mxatom), threshex
      real(8),intent(out) :: overlap(dataguessbs%nao**2)
!
!$OMP parallel do private(jsh)
      do ish= dataguessbs%nshell,1,-1
        do jsh= 1,ish
          call intover1(overlap,coord_g,ish,jsh,threshex,dataguessbs)
        enddo
      enddo
!$OMP end parallel do
      return
end


!---------------------------------------------------------------
  subroutine calcover2(overlap,work,coord_g,threshex, &
&                      datamol,databasis,dataguessbs,datacomp)
!---------------------------------------------------------------
!
! Driver of overlap integral calculation
! (input basis)x(guess basis)
!
! Out : overlap (overlap integral of guess and SCF basis sets)
!       work    (work array)
!
      use modparam, only : mxatom
      use modtype, only : typemol, typebasis, typecomp
      implicit none
      type(typemol),intent(in) :: datamol
      type(typebasis),intent(in) :: databasis, dataguessbs
      type(typecomp),intent(inout) :: datacomp
      integer :: ish, jsh
      real(8),parameter :: zero=0.0D+00
      real(8),intent(in) :: coord_g(3,mxatom), threshex
      real(8),intent(out) :: overlap(databasis%nao*dataguessbs%nao)
      real(8),intent(out) :: work(databasis%nao*dataguessbs%nao)
!
      work(:)= zero
!$OMP parallel
      do ish= dataguessbs%nshell-datacomp%myrank2,1,-datacomp%nproc2
!$OMP do
        do jsh= 1,databasis%nshell
          call intover2(work,coord_g,ish,jsh,threshex,datamol,databasis,dataguessbs)
        enddo
!$OMP enddo
      enddo
!$OMP end parallel
!
      call para_allreducer(work,overlap,databasis%nao*dataguessbs%nao,datacomp%mpi_comm2)
      return
end


